#define u8 unsigned char
#define u16 unsigned short
#define u32 unsigned int
#define u64 unsigned long long
#define s8 signed char
#define s16 signed short
#define s32 signed int
#define s64 signed long long
#ifndef O_BINARY
	#define O_BINARY 0x0
#endif
int hexstr2u32(void* str, void* dat);
int check_elf(void*);
int disasm_elf64(void*, int);
int check_mach(void*);
int disasm_macho64(void*, int);
int check_pe(void*);
int disasm_pe64(void*, int);




void disasm(int argc, char** argv)
{
	u32 at = 0;
	u32 sz = 0;
	if(argc < 2)return;
	if(argc > 2)hexstr2u32(argv[2], &at);
	if(argc > 3)hexstr2u32(argv[3], &sz);
	if(0 == sz)sz = 0x1000000;

	int fd = open(argv[1] , O_RDONLY|O_BINARY);
	if(fd <= 0){
		printf("errno=%d@open\n", errno);
		return;
	}

	u8* buf = malloc(sz);
        if(0 == buf){
		printf("errno=%d@malloc\n", errno);
		goto theend;
	}

	int ret = lseek(fd, at, SEEK_SET);
	if(ret < 0){
		printf("errno=%d@lseek\n", errno);
		goto release;
	}

	ret = read(fd, buf, sz);
	if(ret <= 0){
		printf("errno=%d@read\n", errno);
		goto release;
	}

	if(check_elf(buf)){
		disasm_elf64(buf, ret);
		goto release;
	}
	if(check_mach(buf)){
		disasm_macho64(buf, ret);
		goto release;
	}
	if(check_pe(buf)){
		disasm_pe64(buf, ret);
		goto release;
	}

	printf("unknown filetype, bye bye!\n");

release:
	free(buf);
theend:
	close(fd);
}
